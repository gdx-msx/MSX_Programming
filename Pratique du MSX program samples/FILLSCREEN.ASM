ENASLT	EQU	00024H
MNROM	EQU	0FCC1H
	ORG	0C000H
FIND:
	LD	B,0FH
	LD	HL,04000H
LOOP:	LD	A,B
	OR	080H
	PUSH	BC
	PUSH	AF
	PUSH	HL
	CALL	ENASLT
	POP	HL
	POP	AF
	LD	(HL),A
	LD	B,(HL)
	CP	B
	POP	BC
	JR	Z,RAM
	DJNZ	LOOP
RIEN:
	JR	RETOUR 
RAM:
	LD	IX,04000H
	LD	(IX+0),041H
	LD	(IX+1),042H
	LD	(IX+2),000H
	LD	(IX+3),000H
	LD	(IX+4),000H
	LD	(IX+5),0C1H
	JP	0C100H
;
RETOUR:	LD	A,(MNROM)
	LD	HL,04000H
	CALL	ENASLT
	RET 
;
;
;
	ORG	0C100H
;
FILVRM	EQU	00056H
	PUSH	HL
	LD	HL,WORD
	LD	DE,0FD89H
LOOP2:	LD	A,(DE)
	LD	B,(HL)
	CP	B
	JR	NZ,SYNERR
	CP	0
	INC	HL
	INC	DE
	JR	Z,OUT
	JR	LOOP2
;
SYNERR:	POP	HL
	XOR	A
	CCF
	RET
;
OUT:	POP	HL
	PUSH	HL
	LD	A,(HL)
	CP	028H
	JR	NZ,SYNERR
	INC	HL
	LD	A,(HL)
	CP	022H
	JR	NZ,SYNERR
	INC	HL
	LD	B,(HL)
	INC	HL
	LD	A,(HL)
	CP	022H
	JR	NZ,SYNERR
	INC	HL
	LD	A,(HL)
	CP	029H
	JR	NZ,SYNERR
LOOP3:	INC	HL
	LD	A,(HL)
	CP	020H
	JR	Z,LOOP3
	CP	0
	JR	Z,SUITE
	CP	03AH
	JR	NZ, SYNERR
;
SUITE:	PUSH	HL
	LD	HL,0
	LD	A,B
	LD	BC,00800H
	CALL	FILVRM
	POP	HL
	POP	BC
	XOR	A
	RET
;
WORD:	DB	'FILLSCREEN',000H